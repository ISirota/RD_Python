# число Фібоначчі, рекурсія

def fib(n):
    if n == 0:
        return 0
    elif n == 1:
        return 1
    else:
        return fib(n-1)+fib(n-2)

n = int(input("Ведіть порядковий номер елемента послідовності Фібоначчі "))
print("Елемент послідовності Фібоначчі за порядковим номером ", n, ' = ', fib(n))
